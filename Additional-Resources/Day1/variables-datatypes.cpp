// Variables and Data Types

#include<iostream>
using namespace std;

int main() {
    int age = 18;           // Integer
    float height = 5.9;     // Floating point
    char grade = 'A';       // Character
    bool isPass = true;     // Boolean
    string name = "John";   // String (C++ STL)

    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Grade: " << grade << endl;
    cout << "Passed: " << isPass << endl;

    return 0;
}

