#include<iostream>
using namespace std;

int main(){
    int num1, num2;
    cout << "Enter any 2 integers ";
    cin >> num1 >> num2;
    cout << "Sum of " << num1 << " & " << num2 << ": " << num1 + num2 
    << endl;  
    cout << "Difference of " << num1 << " & " << num2 << ": " << num1 - num2 
    << endl;  
    cout << "Product of " << num1 << " & " << num2 << ": " << num1 * num2 
    << endl;  
    cout << "Quotient of " << num1 << " & " << num2 << ": " << num1 / num2 
    << endl;  
}